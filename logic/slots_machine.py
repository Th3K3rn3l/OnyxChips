import random
from typing import Dict, List

# --- КОНФИГУРАЦИЯ ---
# Веса определяют вероятность. Чем выше вес, тем чаще падает символ.
# Multiplier - на сколько умножается СТАВКА при сборе линии из 3-х таких символов.
SYMBOLS_CONFIG = {
    # Утешительный приз. Игрок должен чувствовать
    # что хоть что-то выигрывает, поэтому выигрыш не 1, а 2
    "cherry": {"weight": 40, "multiplier": 2},
    "lemon": {"weight": 30, "multiplier": 2},
    "orange": {"weight": 20, "multiplier": 2},
    # Плюм уже получше, он дает реальный выигрыш в 15.
    "plum": {"weight": 10, "multiplier": 15},
    # Семерка будет редкой
    "seven": {"weight": 5, "multiplier": 50},
    # Кристалл это джекпот
    "crystal": {"weight": 1, "multiplier": 100},  # Джекпот
}
# Путем брутфорса доказано (мной) что RTP в районе 89-96 %
# Это означает что казино на длинной дистанции всегда будет в плюсе.
# При этом фанаты эпштейна и зверева не разобьют мониторы
# из-за диких процентов проигрышей, т.к ~5-10% не сильно ощущаются

GRID_SIZE = 3


class SlotsEngine:
    def __init__(self):
        self.symbols = list(SYMBOLS_CONFIG.keys())
        self.weights = [s["weight"] for s in SYMBOLS_CONFIG.values()]

    def _generate_grid(self) -> List[List[str]]:
        """Генерирует сетку 3x3 на основе весов символов."""
        # с помощью random.choices генерируем 9 весов для сетки
        flat_grid = random.choices(
            self.symbols, weights=self.weights, k=GRID_SIZE * GRID_SIZE
        )
        # возвращаем сетку с вероятностями
        return [
            flat_grid[i : i + GRID_SIZE] for i in range(0, len(flat_grid), GRID_SIZE)
        ]

    def _check_win(self, grid: List[List[str]]) -> List[Dict]:
        """Проверяет горизонтали и диагонали на наличие выигрышных линий."""
        wins = []

        # 1. Проверка горизонталей (строк)
        for r in range(GRID_SIZE):
            if grid[r][0] == grid[r][1] == grid[r][2]:
                symbol = grid[r][0]
                wins.append(
                    {
                        "line": f"row_{r}",
                        "symbol": symbol,
                        "mult": SYMBOLS_CONFIG[symbol]["multiplier"],
                    }
                )

        # 2. Проверка диагоналей
        if grid[0][0] == grid[1][1] == grid[2][2]:
            symbol = grid[0][0]
            wins.append(
                {
                    "line": "diag_main",
                    "symbol": symbol,
                    "mult": SYMBOLS_CONFIG[symbol]["multiplier"],
                }
            )

        if grid[0][2] == grid[1][1] == grid[2][0]:
            symbol = grid[0][2]
            wins.append(
                {
                    "line": "diag_anti",
                    "symbol": symbol,
                    "mult": SYMBOLS_CONFIG[symbol]["multiplier"],
                }
            )

        return wins

    def spin(self, bet_amount: float) -> Dict:
        """
        Основной метод 'крутить барабаны'.
        Возвращает результат вращения, сумму выигрыша и общую информацию.
        """
        grid = self._generate_grid()
        winning_lines = self._check_win(grid)

        total_multiplier = sum(line["mult"] for line in winning_lines)
        payout = bet_amount * total_multiplier

        return {
            "grid": grid,
            "wins": winning_lines,
            "payout": payout,
            "is_win": payout > 0,
        }


# --- СКРИПТ-СИМУЛЯТОР (RTP Check) ---
def run_simulation(spins: int = 1000, bet: float = 10.0):
    engine = SlotsEngine()
    total_bet = spins * bet
    total_payout = 0
    wins_count = 0

    print(f"🚀 Запуск симуляции: {spins} спинов со ставкой {bet}...")

    for _ in range(spins):
        result = engine.spin(bet)
        total_payout += result["payout"]
        if result["is_win"]:
            wins_count += 1

    rtp = (total_payout / total_bet) * 100

    print("-" * 30)
    print("📊 РЕЗУЛЬТАТЫ:")
    print(f"Сыграно игр: {spins}")
    print(f"Всего поставлено: {total_bet}")
    print(f"Всего выплачено: {total_payout:.2f}")
    print(
        f"Количество выигрышей: {wins_count} (Hit Rate: {(wins_count / spins) * 100:.2f}%)"
    )
    print(f"🔥 RTP: {rtp:.2f}%")
    print("-" * 30)

    if 90 <= rtp <= 95:
        print("✅ Баланс в норме (90-95%)")
    else:
        print("⚠️ Нужна корректировка весов или множителей!")


COUNT_SIMULATION = 10_000
if __name__ == "__main__":
    run_simulation(COUNT_SIMULATION)


